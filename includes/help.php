<h3>How to style your questions</h3>
<p>
paprs. uses markdown to help you format your questions

<h4>Headers</h4>
<p>Use these headers with caution. They can affect the layout of your paper drastically.</p>
<pre># This is an &lt;h1&gt; tag
## This is an &lt;h2&gt; tag
###### This is an &lt;h6&gt; tag
</pre>

<h4>Emphasis</h4>
<pre>*This text will be italic*
_This will also be italic_

**This text will be bold**
__This will also be bold__

*You **can** combine them*
</pre>
will become :<br/>
<em>This text will be italic</em><br/>
<em>This will also be italic</em><br/>
<br/>
<strong>This text will be bold</strong><br/>
<strong>This will also be bold</strong><br/>
<br/>
<em>You <strong>can</strong> combine them</em>
<br/>

<h4>Lists</h4>

<h5>Unordered</h5>
<pre>* Item 1
* Item 2
    * Item 2a
    * Item 2b
</pre>

<h5>Ordered</h5>
<pre>1. Item 1
2. Item 2
3. Item 3
   * Item 3a
   * Item 3b
</pre>

<h4>Tables</h4>
<p>You can create tables by assembling a list of words and dividing them with hyphens <code>-</code> (for the first row), and then separating each column with a pipe <code>|</code>:</p>
<pre>First Header | Second Header
------------ | -------------
Content in cell | Content
column 1 | second column
</pre>

</p>